import mongoose, { Schema } from 'mongoose';
import { Shipping } from './shipping.interface';

const shippingSchema = new Schema<Shipping>(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: false,
    },
    phone: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    country: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    zip: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

export const ShippingModel = mongoose.model('Shipping', shippingSchema);
